class Student {
  final int id;
  final String name;
  final String details;
  final String type;
  final String birthDate;
  final String phoneNumber;
  final String coach;
  final String status;
  final String fee;
  final String result;

  Student({
    required this.id,
    required this.name,
    required this.details,
    required this.type,
    required this.birthDate,
    required this.phoneNumber,
    required this.coach,
    required this.status,
    required this.fee,
    required this.result,
  });
}
